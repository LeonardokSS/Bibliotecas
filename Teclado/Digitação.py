import keyboard

#Para que o código pressione as teclas para você, basta: keyboard.send('tecla')
keyboard.send('a')

#Escreve a palavra a abaixo
keyboard.write('palavra')

#Segura a tecla alt
keyboard.press('alt')

#Solta a tecla alt
keyboard.release('alt')

#Para pressionar uma sequência de teclas basta 
keyboard.press_and_release('ctrl+s')

